# Liste von Zahlen
zahlen = [4, 7, 2, 9, 1, 5, 3]

# Länge der Liste ausgeben
print("Die Länge der Liste ist:", len(zahlen))

# Größte und kleinste Zahl ausgeben
groesste_zahl = max(zahlen)
kleinste_zahl = min(zahlen)

print("Die größte Zahl ist:", groesste_zahl)
print("Die kleinste Zahl ist:", kleinste_zahl)